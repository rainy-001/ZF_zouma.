#ifndef __MY_KEY_HPP__
#define __MY_KEY_HPP__

#include "zf_driver_gpio.hpp"
#include "zf_common_typedef.hpp"

// 按键数量定义
#define KEY_NUM 4
#define KEY_DEBOUNCE_TIME 1     // 消抖时间
#define KEY_LONG_PRESS_TIME 100 // 长按判定时间

// 按键索引映射
#define KEY_UP_IDX     0   // 上键
#define KEY_DOWN_IDX   1   // 下键  
#define KEY_OK_IDX     2   // 确认键
#define KEY_BACK_IDX   3   // 返回键

// 按键状态枚举
typedef enum {
    KEY_RELEASED = 0,
    KEY_DEBOUNCE,
    KEY_PRESSED,
    KEY_LONG_PRESS
} KeyState;

// 按键信息结构体
typedef struct {
    KeyState state;
    uint16 counter;
    uint8 short_press_flag;
    uint8 long_press_flag;
} KeyInfo;

// 菜单动作枚举
typedef enum {
    // 短按动作
    MENU_UP,     // 0
    MENU_DOWN,   // 1
    MENU_OK,     // 2
    MENU_BACK,   // 3
    WAITING,     // 4

    // 长按动作（松开后触发）
    MENU_UP_LONG,    // 5
    MENU_DOWN_LONG,  // 6
    MENU_OK_LONG,    // 7
    MENU_BACK_LONG   // 8
} MenuAction;

class MyKey {
private:
    zf_driver_gpio* key_gpio[KEY_NUM];  // GPIO驱动对象数组
    KeyInfo keys[KEY_NUM];              // 按键状态信息
    
    // 禁用拷贝构造和赋值
    MyKey(const MyKey&) = delete;
    MyKey& operator=(const MyKey&) = delete;

public:
//-------------------------------------------------------------------------------------------------------------------
// 函数简介 构造函数
// 参数说明 无
// 返回参数 无
// 使用示例 MyKey key_manager;
// 备注信息 初始化按键GPIO对象和状态信息
//-------------------------------------------------------------------------------------------------------------------
    MyKey();

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 析构函数
// 参数说明 无
// 返回参数 无
// 使用示例 自动调用
// 备注信息 释放GPIO对象资源
//-------------------------------------------------------------------------------------------------------------------
    ~MyKey();

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 按键扫描函数
// 参数说明 无
// 返回参数 无
// 使用示例 key_manager.scan_keys();
// 备注信息 使用状态机实现按键消抖和长短按检测，需要定时调用
//-------------------------------------------------------------------------------------------------------------------
    void scan_keys(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 获取菜单动作
// 参数说明 无
// 返回参数 MenuAction 菜单动作枚举值
// 使用示例 MenuAction action = key_manager.get_menu_action();
// 备注信息 将按键状态转换为菜单动作，会自动清除按键标志位
//-------------------------------------------------------------------------------------------------------------------
    MenuAction get_menu_action(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 清除指定按键的长按标志
// 参数说明 key_id 按键索引
// 返回参数 无
// 使用示例 key_manager.clear_long_press_flag(KEY_UP_IDX);
// 备注信息 手动清除特定按键的长按标志位
//-------------------------------------------------------------------------------------------------------------------
    void clear_long_press_flag(uint8 key_id);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 清除所有按键的长按标志
// 参数说明 无
// 返回参数 无
// 使用示例 key_manager.clear_all_long_press_flags();
// 备注信息 清除所有按键的长按标志位
//-------------------------------------------------------------------------------------------------------------------
    void clear_all_long_press_flags(void);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 获取按键状态信息
// 参数说明 key_id 按键索引
// 返回参数 KeyInfo* 按键状态信息指针
// 使用示例 KeyInfo* info = key_manager.get_key_info(KEY_UP_IDX);
// 备注信息 获取指定按键的详细状态信息
//-------------------------------------------------------------------------------------------------------------------
    KeyInfo* get_key_info(uint8 key_id);
};

#endif