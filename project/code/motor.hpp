#ifndef __MOTOR_H_HPP__
#define __MOTOR_H_HPP__

/* ================================================================================================================
 *                                           头文件包含
 * ================================================================================================================ */
#include "zf_common_typedef.hpp"
#include "zf_driver_pwm.hpp"
#include "zf_driver_gpio.hpp"
#include "zf_driver_encoder.hpp"
#include <math.h>
/* ================================================================================================================
 *                                           全局对象声明
 * ================================================================================================================ */
// 电机PWM控制对象（注意：硬件接线导致左右互换）
extern zf_driver_pwm right_motor;      // 右电机PWM控制对象
extern zf_driver_pwm left_motor;       // 左电机PWM控制对象

// 电机方向控制对象
extern zf_driver_gpio right_dir;       // 右电机方向控制（0=正转，1=反转）
extern zf_driver_gpio left_dir;        // 左电机方向控制（0=正转，1=反转）

/* ================================================================================================================
 *                                           函数声明
 * ================================================================================================================ */

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     电机初始化
// 参数说明     无
// 返回参数     bool        初始化成功返回true
// 使用示例     motor_init();
// 备注信息     初始化电机PWM和方向GPIO，设置初始状态为停止
//-------------------------------------------------------------------------------------------------------------------
bool motor_init();

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     设置电机速度（PID控制器输出接口）
// 参数说明     left_speed  左轮速度指令，范围-120~120（负值表示反转）
// 参数说明     right_speed 右轮速度指令，范围-120~120（负值表示反转）
// 返回参数     void
// 使用示例     motor_set_speed(50, 50);  // 双轮前进
// 备注信息     内部会进行速度→PWM映射、死区处理、限幅保护
//              注意：由于硬件接线，left_speed控制右电机，right_speed控制左电机
//-------------------------------------------------------------------------------------------------------------------
void motor_set_speed(int16_t left_speed, int16_t right_speed);

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     停止电机
// 参数说明     无
// 返回参数     void
// 使用示例     motor_stop();
// 备注信息     立即停止电机，等效于motor_set_speed(0, 0)
//-------------------------------------------------------------------------------------------------------------------
void motor_stop();

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     获取电机速度（编码器采集+速度映射）
// 参数说明     right_speed      指向右轮速度变量的指针（输出参数）
// 参数说明     left_speed       指向左轮速度变量的指针（输出参数）
// 参数说明     sampling_period  采样周期(ms)，建议≤1000避免int16溢出
// 返回参数     bool            采集成功返回true，失败返回false
// 使用示例     get_and_remap_speed(&right_speed, &left_speed, 3);
// 备注信息     必须在定时中断中周期性调用，采样后会自动清零编码器计数
//              速度值 = 编码器计数 × 权重系数
//-------------------------------------------------------------------------------------------------------------------
bool get_and_remap_speed(float* right_speed, float* left_speed, int16 sampling_period);

//信号发生器，用于测试电机以及电机对磁力轴的影响
float motor_test_signal_generator(int mode, float period_ms);
float speed_decision(int num, float max_speed, float min_speed);
void brushless_init();
void esc_set_power(float power);

void lardc_get_speed(uint8_t T, float encoder_cpr, float wheel_radius, 
                     float ts, float *left_speed, float *right_speed);
void motor_set_speed_ladrc(float left_pwm, float right_pwm);
#endif  // __MOTOR_H_HPP__