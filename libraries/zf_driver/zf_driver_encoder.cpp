//-------------------------------------------------------------------------------------------------------------------
// 函数简介 获取编码器计数值
// 参数说明 无
// 返回参数 int16 编码器当前计数值
// 使用示例 int16 cnt = encoder_obj.get_count();
// 备注信息 读取设备文件数据并转换为整型计数值
//-------------------------------------------------------------------------------------------------------------------
int16 zf_driver_encoder::get_count(void)
{
    uint8 buffer[2] = {0};
    
    // 使用基类的 read_buff 方法读取2个字节
    if (read_buff(buffer, sizeof(buffer)) != 0) {
        // 读取失败，返回0
        return 0;
    }
    
    // 关键修复：正确处理字节序
    // 原始数据是小端序，但代码可能按大端序处理了
    // 从您的调试数据看，低位在前（小端序）
    
    // 方法A：如果当前代码是大端序，改为小端序
    // int16 value = (int16)((buffer[0] << 8) | buffer[1]);
    // 或者
    // 方法B：如果当前代码是小端序，但需要符号位处理
    int16 value = (int16)(buffer[0] | (buffer[1] << 8));
    
    return value;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介 清零编码器计数值
// 参数说明 无
// 返回参数 无
// 使用示例 encoder_obj.clear_count();
// 备注信息 向设备文件写入0值，完成编码器计数清零操作
//-------------------------------------------------------------------------------------------------------------------
void zf_driver_encoder::clear_count(void)
{
    int16 zero = 0;
    this->write_buff((uint8*)&zero, sizeof(zero));
}